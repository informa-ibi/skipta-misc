<?php

class FollowABroadcast
{

  /**
   * 
   * @var guid $userguid
   * @access public
   */
  public $userguid;

  /**
   * 
   * @var guid $broadcastguid
   * @access public
   */
  public $broadcastguid;

  /**
   * 
   * @var int $score
   * @access public
   */
  public $score;

  /**
   * 
   * @param guid $userguid
   * @param guid $broadcastguid
   * @param int $score
   * @access public
   */
  public function __construct($userguid, $broadcastguid, $score)
  {
    $this->userguid = $userguid;
    $this->broadcastguid = $broadcastguid;
    $this->score = $score;
  }

}

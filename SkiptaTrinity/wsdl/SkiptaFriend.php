<?php

class SkiptaFriend
{

  /**
   * 
   * @var guid $WorldId
   * @access public
   */
  public $WorldId;

  /**
   * 
   * @var string $Name
   * @access public
   */
  public $Name;

  /**
   * 
   * @var guid $UserId
   * @access public
   */
  public $UserId;

  /**
   * 
   * @param guid $WorldId
   * @param string $Name
   * @param guid $UserId
   * @access public
   */
  public function __construct($WorldId, $Name, $UserId)
  {
    $this->WorldId = $WorldId;
    $this->Name = $Name;
    $this->UserId = $UserId;
  }

}

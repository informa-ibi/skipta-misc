<?php

class AddWidgetToUser
{

  /**
   * 
   * @var ClientSkiptaSession $session
   * @access public
   */
  public $session;

  /**
   * 
   * @var guid $widgetId
   * @access public
   */
  public $widgetId;

  /**
   * 
   * @param ClientSkiptaSession $session
   * @param guid $widgetId
   * @access public
   */
  public function __construct($session, $widgetId)
  {
    $this->session = $session;
    $this->widgetId = $widgetId;
  }

}

<?php

class SkiptaNewsFeedItem
{

  /**
   * 
   * @var guid $NewsFeedId
   * @access public
   */
  public $NewsFeedId;

  /**
   * 
   * @var string $NewsGroup
   * @access public
   */
  public $NewsGroup;

  /**
   * 
   * @var guid $NewsIdentifier
   * @access public
   */
  public $NewsIdentifier;

  /**
   * 
   * @var string $ItemIdentifier
   * @access public
   */
  public $ItemIdentifier;

  /**
   * 
   * @var string $Title
   * @access public
   */
  public $Title;

  /**
   * 
   * @var string $Link
   * @access public
   */
  public $Link;

  /**
   * 
   * @var string $Description
   * @access public
   */
  public $Description;

  /**
   * 
   * @var string $Comment
   * @access public
   */
  public $Comment;

  /**
   * 
   * @var dateTime $PublishedDate
   * @access public
   */
  public $PublishedDate;

  /**
   * 
   * @var boolean $IsPermaLink
   * @access public
   */
  public $IsPermaLink;

  /**
   * 
   * @var int $Likes
   * @access public
   */
  public $Likes;

  /**
   * 
   * @var int $DisLikes
   * @access public
   */
  public $DisLikes;

  /**
   * 
   * @var string $UserRated
   * @access public
   */
  public $UserRated;

  /**
   * 
   * @var int $Comments
   * @access public
   */
  public $Comments;

  /**
   * 
   * @var int $Popularity
   * @access public
   */
  public $Popularity;

  /**
   * 
   * @param guid $NewsFeedId
   * @param string $NewsGroup
   * @param guid $NewsIdentifier
   * @param string $ItemIdentifier
   * @param string $Title
   * @param string $Link
   * @param string $Description
   * @param string $Comment
   * @param dateTime $PublishedDate
   * @param boolean $IsPermaLink
   * @param int $Likes
   * @param int $DisLikes
   * @param string $UserRated
   * @param int $Comments
   * @param int $Popularity
   * @access public
   */
  public function __construct($NewsFeedId, $NewsGroup, $NewsIdentifier, $ItemIdentifier, $Title, $Link, $Description, $Comment, $PublishedDate, $IsPermaLink, $Likes, $DisLikes, $UserRated, $Comments, $Popularity)
  {
    $this->NewsFeedId = $NewsFeedId;
    $this->NewsGroup = $NewsGroup;
    $this->NewsIdentifier = $NewsIdentifier;
    $this->ItemIdentifier = $ItemIdentifier;
    $this->Title = $Title;
    $this->Link = $Link;
    $this->Description = $Description;
    $this->Comment = $Comment;
    $this->PublishedDate = $PublishedDate;
    $this->IsPermaLink = $IsPermaLink;
    $this->Likes = $Likes;
    $this->DisLikes = $DisLikes;
    $this->UserRated = $UserRated;
    $this->Comments = $Comments;
    $this->Popularity = $Popularity;
  }

}

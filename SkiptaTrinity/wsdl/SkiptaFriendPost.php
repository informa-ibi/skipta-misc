<?php

class SkiptaFriendPost
{

  /**
   * 
   * @var guid $UserId
   * @access public
   */
  public $UserId;

  /**
   * 
   * @var guid $WorldId
   * @access public
   */
  public $WorldId;

  /**
   * 
   * @var int $StartRow
   * @access public
   */
  public $StartRow;

  /**
   * 
   * @var int $EndRow
   * @access public
   */
  public $EndRow;

  /**
   * 
   * @var string $DelimitedPostGuids
   * @access public
   */
  public $DelimitedPostGuids;

  /**
   * 
   * @var SkiptaPostQueryOption $SkiptaPostQueryOption
   * @access public
   */
  public $SkiptaPostQueryOption;

  /**
   * 
   * @param guid $UserId
   * @param guid $WorldId
   * @param int $StartRow
   * @param int $EndRow
   * @param string $DelimitedPostGuids
   * @param SkiptaPostQueryOption $SkiptaPostQueryOption
   * @access public
   */
  public function __construct($UserId, $WorldId, $StartRow, $EndRow, $DelimitedPostGuids, $SkiptaPostQueryOption)
  {
    $this->UserId = $UserId;
    $this->WorldId = $WorldId;
    $this->StartRow = $StartRow;
    $this->EndRow = $EndRow;
    $this->DelimitedPostGuids = $DelimitedPostGuids;
    $this->SkiptaPostQueryOption = $SkiptaPostQueryOption;
  }

}

<?php

class SkiptaWorldMenusResponse
{

  /**
   * 
   * @var guid $WorldID
   * @access public
   */
  public $WorldID;

  /**
   * 
   * @var menus $menus
   * @access public
   */
  public $menus;

  /**
   * 
   * @param guid $WorldID
   * @param menus $menus
   * @access public
   */
  public function __construct($WorldID, $menus)
  {
    $this->WorldID = $WorldID;
    $this->menus = $menus;
  }

}

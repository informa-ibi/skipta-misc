<?php

class SkiptaUserRatingResponse
{

  /**
   * 
   * @var array $RatedUsers
   * @access public
   */
  public $RatedUsers;

  /**
   * 
   * @var int $Likes
   * @access public
   */
  public $Likes;

  /**
   * 
   * @var int $Dislikes
   * @access public
   */
  public $Dislikes;

  /**
   * 
   * @var string $UserRating
   * @access public
   */
  public $UserRating;

  /**
   * 
   * @var int $Comments
   * @access public
   */
  public $Comments;

  /**
   * 
   * @var array $CommentsInfo
   * @access public
   */
  public $CommentsInfo;

  /**
   * 
   * @param array $RatedUsers
   * @param int $Likes
   * @param int $Dislikes
   * @param string $UserRating
   * @param int $Comments
   * @param array $CommentsInfo
   * @access public
   */
  public function __construct($RatedUsers, $Likes, $Dislikes, $UserRating, $Comments, $CommentsInfo)
  {
    $this->RatedUsers = $RatedUsers;
    $this->Likes = $Likes;
    $this->Dislikes = $Dislikes;
    $this->UserRating = $UserRating;
    $this->Comments = $Comments;
    $this->CommentsInfo = $CommentsInfo;
  }

}

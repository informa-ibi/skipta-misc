<?php

class SkiptaOption
{

  /**
   * 
   * @var string $Id
   * @access public
   */
  public $Id;

  /**
   * 
   * @var string $Name
   * @access public
   */
  public $Name;

  /**
   * 
   * @param string $Id
   * @param string $Name
   * @access public
   */
  public function __construct($Id, $Name)
  {
    $this->Id = $Id;
    $this->Name = $Name;
  }

}

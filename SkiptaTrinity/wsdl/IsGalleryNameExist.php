<?php

class IsGalleryNameExist
{

  /**
   * 
   * @var guid $userid
   * @access public
   */
  public $userid;

  /**
   * 
   * @var string $galleryname
   * @access public
   */
  public $galleryname;

  /**
   * 
   * @param guid $userid
   * @param string $galleryname
   * @access public
   */
  public function __construct($userid, $galleryname)
  {
    $this->userid = $userid;
    $this->galleryname = $galleryname;
  }

}

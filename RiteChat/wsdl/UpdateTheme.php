<?php

class UpdateTheme
{

  /**
   * 
   * @var guid $worldId
   * @access public
   */
  public $worldId;

  /**
   * 
   * @var string $themeName
   * @access public
   */
  public $themeName;

  /**
   * 
   * @param guid $worldId
   * @param string $themeName
   * @access public
   */
  public function __construct($worldId, $themeName)
  {
    $this->worldId = $worldId;
    $this->themeName = $themeName;
  }

}

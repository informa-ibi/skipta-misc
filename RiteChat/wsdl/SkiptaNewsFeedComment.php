<?php

class SkiptaNewsFeedComment
{

  /**
   * 
   * @var guid $NewsCommentId
   * @access public
   */
  public $NewsCommentId;

  /**
   * 
   * @var guid $NewsItemId
   * @access public
   */
  public $NewsItemId;

  /**
   * 
   * @var guid $UserId
   * @access public
   */
  public $UserId;

  /**
   * 
   * @var guid $WorldPosted
   * @access public
   */
  public $WorldPosted;

  /**
   * 
   * @var string $Link
   * @access public
   */
  public $Link;

  /**
   * 
   * @var string $Comment
   * @access public
   */
  public $Comment;

  /**
   * 
   * @var dateTime $CreatedDate
   * @access public
   */
  public $CreatedDate;

  /**
   * 
   * @var string $Firstname
   * @access public
   */
  public $Firstname;

  /**
   * 
   * @var string $Lastname
   * @access public
   */
  public $Lastname;

  /**
   * 
   * @var int $Likes
   * @access public
   */
  public $Likes;

  /**
   * 
   * @var int $DisLikes
   * @access public
   */
  public $DisLikes;

  /**
   * 
   * @var string $UserRated
   * @access public
   */
  public $UserRated;

  /**
   * 
   * @param guid $NewsCommentId
   * @param guid $NewsItemId
   * @param guid $UserId
   * @param guid $WorldPosted
   * @param string $Link
   * @param string $Comment
   * @param dateTime $CreatedDate
   * @param string $Firstname
   * @param string $Lastname
   * @param int $Likes
   * @param int $DisLikes
   * @param string $UserRated
   * @access public
   */
  public function __construct($NewsCommentId, $NewsItemId, $UserId, $WorldPosted, $Link, $Comment, $CreatedDate, $Firstname, $Lastname, $Likes, $DisLikes, $UserRated)
  {
    $this->NewsCommentId = $NewsCommentId;
    $this->NewsItemId = $NewsItemId;
    $this->UserId = $UserId;
    $this->WorldPosted = $WorldPosted;
    $this->Link = $Link;
    $this->Comment = $Comment;
    $this->CreatedDate = $CreatedDate;
    $this->Firstname = $Firstname;
    $this->Lastname = $Lastname;
    $this->Likes = $Likes;
    $this->DisLikes = $DisLikes;
    $this->UserRated = $UserRated;
  }

}

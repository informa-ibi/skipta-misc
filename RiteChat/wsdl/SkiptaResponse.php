<?php

class SkiptaResponse
{

  /**
   * 
   * @var SkiptaWorld $World
   * @access public
   */
  public $World;

  /**
   * 
   * @var SkiptaResponseCode $ResponseCode
   * @access public
   */
  public $ResponseCode;

  /**
   * 
   * @var string $ResponseMessage
   * @access public
   */
  public $ResponseMessage;

  /**
   * 
   * @param SkiptaWorld $World
   * @param SkiptaResponseCode $ResponseCode
   * @param string $ResponseMessage
   * @access public
   */
  public function __construct($World, $ResponseCode, $ResponseMessage)
  {
    $this->World = $World;
    $this->ResponseCode = $ResponseCode;
    $this->ResponseMessage = $ResponseMessage;
  }

}

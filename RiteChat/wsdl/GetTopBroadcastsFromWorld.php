<?php

class GetTopBroadcastsFromWorld
{

  /**
   * 
   * @var ClientSkiptaSession $session
   * @access public
   */
  public $session;

  /**
   * 
   * @var ClientSkiptaWorld $world
   * @access public
   */
  public $world;

  /**
   * 
   * @var int $maxresults
   * @access public
   */
  public $maxresults;

  /**
   * 
   * @var string $sortBy
   * @access public
   */
  public $sortBy;

  /**
   * 
   * @param ClientSkiptaSession $session
   * @param ClientSkiptaWorld $world
   * @param int $maxresults
   * @param string $sortBy
   * @access public
   */
  public function __construct($session, $world, $maxresults, $sortBy)
  {
    $this->session = $session;
    $this->world = $world;
    $this->maxresults = $maxresults;
    $this->sortBy = $sortBy;
  }

}

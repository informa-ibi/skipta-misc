<?php

class SkiptaFriendMessage
{

  /**
   * 
   * @var dateTime $TrashDate
   * @access public
   */
  public $TrashDate;

  /**
   * 
   * @var string $FromLastName
   * @access public
   */
  public $FromLastName;

  /**
   * 
   * @var string $FromFirstName
   * @access public
   */
  public $FromFirstName;

  /**
   * 
   * @var string $ToLastName
   * @access public
   */
  public $ToLastName;

  /**
   * 
   * @var string $ToFirstName
   * @access public
   */
  public $ToFirstName;

  /**
   * 
   * @var guid $MessageID
   * @access public
   */
  public $MessageID;

  /**
   * 
   * @var string $MessageText
   * @access public
   */
  public $MessageText;

  /**
   * 
   * @var string $MessageSubject
   * @access public
   */
  public $MessageSubject;

  /**
   * 
   * @var dateTime $SentDate
   * @access public
   */
  public $SentDate;

  /**
   * 
   * @var dateTime $RecievedDate
   * @access public
   */
  public $RecievedDate;

  /**
   * 
   * @var guid $FromUID
   * @access public
   */
  public $FromUID;

  /**
   * 
   * @var guid $ToUID
   * @access public
   */
  public $ToUID;

  /**
   * 
   * @var guid $ParentMessageID
   * @access public
   */
  public $ParentMessageID;

  /**
   * 
   * @var FriendMessageStatus $SenderStatus
   * @access public
   */
  public $SenderStatus;

  /**
   * 
   * @var FriendMessageStatus $ReceiverStatus
   * @access public
   */
  public $ReceiverStatus;

  /**
   * 
   * @param dateTime $TrashDate
   * @param string $FromLastName
   * @param string $FromFirstName
   * @param string $ToLastName
   * @param string $ToFirstName
   * @param guid $MessageID
   * @param string $MessageText
   * @param string $MessageSubject
   * @param dateTime $SentDate
   * @param dateTime $RecievedDate
   * @param guid $FromUID
   * @param guid $ToUID
   * @param guid $ParentMessageID
   * @param FriendMessageStatus $SenderStatus
   * @param FriendMessageStatus $ReceiverStatus
   * @access public
   */
  public function __construct($TrashDate, $FromLastName, $FromFirstName, $ToLastName, $ToFirstName, $MessageID, $MessageText, $MessageSubject, $SentDate, $RecievedDate, $FromUID, $ToUID, $ParentMessageID, $SenderStatus, $ReceiverStatus)
  {
    $this->TrashDate = $TrashDate;
    $this->FromLastName = $FromLastName;
    $this->FromFirstName = $FromFirstName;
    $this->ToLastName = $ToLastName;
    $this->ToFirstName = $ToFirstName;
    $this->MessageID = $MessageID;
    $this->MessageText = $MessageText;
    $this->MessageSubject = $MessageSubject;
    $this->SentDate = $SentDate;
    $this->RecievedDate = $RecievedDate;
    $this->FromUID = $FromUID;
    $this->ToUID = $ToUID;
    $this->ParentMessageID = $ParentMessageID;
    $this->SenderStatus = $SenderStatus;
    $this->ReceiverStatus = $ReceiverStatus;
  }

}

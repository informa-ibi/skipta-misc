<?php

class UpdateResourceMenuItemDisplayName
{

  /**
   * 
   * @var ClientSkiptaSession $session
   * @access public
   */
  public $session;

  /**
   * 
   * @var guid $worldId
   * @access public
   */
  public $worldId;

  /**
   * 
   * @var int $menuItemId
   * @access public
   */
  public $menuItemId;

  /**
   * 
   * @var string $menuName
   * @access public
   */
  public $menuName;

  /**
   * 
   * @param ClientSkiptaSession $session
   * @param guid $worldId
   * @param int $menuItemId
   * @param string $menuName
   * @access public
   */
  public function __construct($session, $worldId, $menuItemId, $menuName)
  {
    $this->session = $session;
    $this->worldId = $worldId;
    $this->menuItemId = $menuItemId;
    $this->menuName = $menuName;
  }

}
